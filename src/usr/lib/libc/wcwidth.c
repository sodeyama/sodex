static int width_interval_contains(const struct width_interval *table, int count,
                                   u_int32_t codepoint)
{
  int left = 0;
  int right = count - 1;

  while (left <= right) {
    int mid = left + (right - left) / 2;
    if (codepoint < table[mid].first) {
      right = mid - 1;
    } else if (codepoint > table[mid].last) {
      left = mid + 1;
    } else {
      return 1;
    }
  }
  return 0;
}

int unicode_wcwidth(u_int32_t codepoint)
{
  if (codepoint == 0)
    return 0;
  if (codepoint < 0x20U || (codepoint >= 0x7fU && codepoint < 0xa0U))
    return -1;
  if (width_interval_contains(zero_width_table,
                              (int)(sizeof(zero_width_table) / sizeof(zero_width_table[0])),
                              codepoint) != 0)
    return 0;
  if (width_interval_contains(wide_table,
                              (int)(sizeof(wide_table) / sizeof(wide_table[0])),
                              codepoint) != 0)
    return 2;
  return 1;
}
